/**
 * 지도 맵 영역: Figma Map Shadow 그룹의 absoluteBoundingBox.
 * viewBox 변환은 SimpleMapView에서만 수행.
 */
export const FIGMA_MAP_BOUNDS = { x: 69.91, y: 102.74, width: 1872.96, height: 796.09 } as const;

/** 지도 viewBox 크기 (Figma bounds와 동일 비율로 x·y 스케일 일치) */
export const VIEWBOX_W = 1000;
export const VIEWBOX_H = (VIEWBOX_W * FIGMA_MAP_BOUNDS.height) / FIGMA_MAP_BOUNDS.width;

const SCALE = VIEWBOX_W / FIGMA_MAP_BOUNDS.width;

/** 국가 코드에 해당하는 지도 상의 viewBox 중심 좌표. 레이아웃에 없으면 null */
export function getCountryViewBoxCenter(code: string): { x: number; y: number } | null {
  const entry = FIGMA_COUNTRY_LAYOUT.find((e) => e.code === code.toLowerCase());
  if (!entry) return null;
  const x = (entry.x - FIGMA_MAP_BOUNDS.x) * SCALE;
  const y = (entry.y - FIGMA_MAP_BOUNDS.y) * SCALE;
  const w = entry.width * SCALE;
  const h = entry.height * SCALE;
  return { x: x + w / 2, y: y + h / 2 };
}

